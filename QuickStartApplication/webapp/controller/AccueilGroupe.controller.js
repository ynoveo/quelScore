onNavToGroup: function (oEvent) {
			var sPath = oEvent.getSource().getBindingContext().getPath();
            var oModel = this.getView().getModel();
            var oContext = oModel.getProperty(sPath);
            var obj = JSON.parse(JSON.stringify(oContext));
				if (obj.isSpecial === "All") {
					sap.ui.core.UIComponent.getRouterFor(this).navTo("mesGroupes");
				} 
				else if (obj.isSpecial === "Create") {
					this.onNewPressed();
				}
				else {
					sap.ui.core.UIComponent.getRouterFor(this).navTo("UserList", {
				groupId: obj.idGroup
			});
				}
			
		},

